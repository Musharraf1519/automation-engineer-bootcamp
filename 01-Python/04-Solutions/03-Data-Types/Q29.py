# | **Data Type**         | **Description**                                                 | **Example**                    |
# | --------------------- | --------------------------------------------------------------- | ------------------------------ |
# | **Integer (`int`)**   | Stores whole numbers (positive, negative, or zero).             | `25`, `-10`, `0`               |
# | **Float (`float`)**   | Stores numbers with decimal points.                             | `98.5`, `3.14`, `-7.25`        |
# | **String (`str`)**    | Stores text (letters, numbers, and symbols) enclosed in quotes. | `"Rahul"`, `"Hello"`, `"A123"` |
# | **Boolean (`bool`)**  | Stores one of two logical values: `True` or `False`.            | `True`, `False`                |
# | **None (`NoneType`)** | Represents the absence of a value.                              | `None`                         |

### Example in Python

age = 25                  # Integer
height = 5.8              # Float
name = "Rahul"            # String
is_student = True         # Boolean
manager = None            # NoneType
